from torch.utils.cpp_extension import load_inline

# Define the custom CUDA kernel for GELU activation
gelu_source = """
#include <torch/extension.h>
#include <cuda_runtime.h>
#include <math.h>

__global__ 
void gelu_forward_kernel(const float* __restrict__ input, float* output, int size) {
    const int idx = blockIdx.x * blockDim.x + threadIdx.x;
    if (idx < size) {
        const float x = input[idx];
        const float x_cubed = x * x * x;
        const float poly = 0.044715 * x_cubed;
        const float inner = x + poly;
        const float tanh_val = tanh(sqrtf(2.0f / M_PI) * inner);
        output[idx] = 0.5f * x * (1.0f + tanh_val);
    }
}

torch::Tensor gelu_forward_cuda(torch::Tensor input) {
    const int size = input.numel();
    const int block_size = 256;
    const int num_blocks = (size + block_size - 1) / block_size;

    auto output = torch::empty_like(input);
    gelu_forward_kernel<<<num_blocks, block_size>>>(input.data_ptr<float>(), output.data_ptr<float>(), size);

    return output;
}
"""

gelu_cpp_source = "torch::Tensor gelu_forward_cuda(torch::Tensor input);"

# Compile the inline CUDA code for GELU
gelu_cuda = load_inline(
    name="gelu_cuda",
    cpp_sources=gelu_cpp_source,
    cuda_sources=gelu_source,
    functions=["gelu_forward_cuda"],
    verbose=True,
    extra_cflags=["-O3"],
    extra_ldflags=[],
)

class ModelNew(nn.Module):
    def __init__(self):
        super(ModelNew, self).__init__()
        self.gelu_cuda = gelu_cuda

    def forward(self, x):
        return self.gelu_cuda.gelu_forward_cuda(x)

